void Icons::DrawIcon(const byte data[16][16], int xpos, int ypos, uint8_t r, uint8_t g, uint8_t b)
{
	for (int y = 0; y < 16; y++)
	{
		for (int x = 0; x < 16; x++)
		{
			uint16_t c = pgm_read_byte_near(&(data[y][x]));
			Display.setColor(c * r / 256, c * g / 256, c * b / 256);
			Display.drawPixel(xpos + x, ypos + y);
		}
	}
}
